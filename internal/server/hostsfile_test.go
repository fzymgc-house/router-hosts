func TestFormatHostsFile_BasicEntries(t *testing.T) {
	gen := NewHostsFileGenerator("/tmp/test-hosts")
	comment := "NAS storage"
	entries := []domain.HostEntry{
		{
			ID:       ulid.Make(),
			IP:       "192.168.1.10",
			Hostname: "server.local",
		},
		{
			ID:       ulid.Make(),
			IP:       "192.168.1.20",
			Hostname: "nas.local",
			Comment:  &comment,
			Tags:     []string{"homelab"},
		},
	}

	content := gen.FormatHostsFile(entries)
	assert.Contains(t, content, "# Entry count: 2")
	assert.Contains(t, content, "192.168.1.10\tserver.local\n")
	assert.Contains(t, content, "192.168.1.20\tnas.local\t# NAS storage [homelab]\n")
}

